CREATE SCHEMA IF NOT EXISTS raw_fintrust;

CREATE TABLE IF NOT EXISTS raw_fintrust.customers (
 customer_id STRING,
 full_name STRING,
 city STRING,
 segment STRING,
 monthly_income NUMERIC,
 created_at DATE
);
